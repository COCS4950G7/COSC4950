##################Naming Convention for printing messages on the screen###########################
#STATUS - displaying messages that state what the server/client is doing
#INFO - displaying messages that display information to the screen
#WARNING - displaying that something unexpected has occurred but it isn't a critical error
#ERROR - display error message, indicates that something in the code has caused the program to entered the except block
#I/O - display a message that states information about the I/O system
